          $objWriter->save($this->Xls_f);
      } 
      else 
      { 
          $_SESSION['scriptcase']['export_return'] = $this->arr_export;
      } 
      if(isset($_SESSION['sc_session'][$this->Ini->sc_page]['app_grid_sec_users']['export_sel_columns']['field_order']))
      {
          $_SESSION['sc_session'][$this->Ini->sc_page]['app_grid_sec_users']['field_order'] = $_SESSION['sc_session'][$this->Ini->sc_page]['app_grid_sec_users']['export_sel_columns']['field_order'];
          unset($_SESSION['sc_session'][$this->Ini->sc_page]['app_grid_sec_users']['export_sel_columns']['field_order']);
      }
      if(isset($_SESSION['sc_session'][$this->Ini->sc_page]['app_grid_sec_users']['export_sel_columns']['usr_cmp_sel']))
