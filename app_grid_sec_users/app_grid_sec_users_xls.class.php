          $this->arr_export['lines'][$this->Xls_row][1]['format'] = "";
      }
      if (isset($this->NM_Col_din) && !$_SESSION['sc_session'][$this->Ini->sc_page]['app_grid_sec_users']['embutida'])
      { 
          foreach ($this->NM_Col_din as $col => $width)
          { 
