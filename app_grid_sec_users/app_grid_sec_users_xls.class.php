          {
              $this->login = substr($this->login, 0, $tmp_pos);
          }
          $this->name = (isset($Busca_temp['name'])) ? $Busca_temp['name'] : ""; 
          $tmp_pos = (is_string($this->name)) ? strpos($this->name, "##@@") : false;
