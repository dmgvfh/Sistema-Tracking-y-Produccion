          $this->Arq_zip = $_SESSION['sc_session'][$this->Ini->sc_page]['app_grid_sec_users']['xls_name'];
          $this->Tit_doc = $_SESSION['sc_session'][$this->Ini->sc_page]['app_grid_sec_users']['xls_name'];
          $Pos = strrpos($_SESSION['sc_session'][$this->Ini->sc_page]['app_grid_sec_users']['xls_name'], ".");
